# -*- coding: utf-8 -*-
from __future__ import annotations

from chat_agent.app.prompts.commands_catalog import PLANNER_TOOLS_SNIPPET


def build_planner_system(*, extra_env_system: str) -> str:
    """System-сообщение для шага planner (JSON)."""
    core = f"""Ты planner Telegram-бота по курсам RUB↔THB. Верни ТОЛЬКО один JSON-объект без markdown и без текста вокруг.

Формат:
{{"tool":"<строка>","arguments":{{}},"needs_tool":true|false,"think":true|false,"out_of_scope":true|false}}
Опционально: `"tool_steps":[{{"tool":"…","arguments":{{}}}}]` (2–5 шагов). Если `tool_steps` есть, исполняются именно они.

Базовые правила:
1) `out_of_scope=true` только если реплика не про курсы/обмен из домена бота.
   Тогда обязательно: `tool="none"`, `needs_tool=false`, `arguments={{}}`, `think=false`.
2) Для коротких общих запросов «курс», «курс валюты», «какой курс», «курс бата» выбирай `get_rates_summary` с `arguments={{}}`, `needs_tool=true`, обычно `think=false`.
3) `arguments` заполняй только данными из текущей реплики; лишние поля запрещены.
4) Если нужен сценарий из нескольких команд (`tool_steps`), ставь `needs_tool=true` и `think=true`.
5) Если вопрос в теме, но без нужных параметров/без необходимости вызывать rates.py — `tool="none"`, `needs_tool=false`, `out_of_scope=false`, `think` по смыслу.
6) Если в реплике явно упомянут Avosend (авосенд/avosend) и есть сумма для расчёта — выбирай `get_avosend_report`; для «получение в отделении» ставь `mode="cash"` и обычно `think=true`.
7) Если явно упомянут конкретный источник (`koronapay/корона`, `ex24`, `kwikpay`, `askmoney`) — выбирай одноимённый tool источника, а не общую сводку.

Инструменты и точные правила выбора:
{PLANNER_TOOLS_SNIPPET}
"""
    extra = (extra_env_system or "").strip()
    if extra:
        return extra + "\n\n---\n\n" + core
    return core


# Ответ уходит в Telegram с parse_mode=HTML (см. reply_parse_mode в API) — не путать с Markdown.
_RESPONDER_TELEGRAM_HTML = """
Формат текста — только Telegram HTML (не GitHub/CommonMark):
- Не используй `###`/`##`/`#`, не начинай строки с `* ` как маркер списка, не пиши `**жирный**` или `_курсив_` в markdown-смысле — в Telegram это не сработает или сломает разбор.
- Жирное выделение: `<b>текст</b>`; курсив: `<i>текст</i>`; моноширинный фрагмент: `<code>…</code>`; многострочный блок: `<pre>…</pre>`. Ссылка: `<a href="https://…">подпись</a>`.
- Заголовок секции — отдельная строка вида `<b>Почему …</b>` без решёток.
- Списки: нумерация `1.`, `2.` или маркер `•` в начале строки обычным текстом (теги списков HTML не поддерживаются клиентом).
- Символы `<`, `>`, `&` вне тегов экранируй как `&lt;`, `&gt;`, `&amp;`.
- Теги должны быть корректно закрыты; кроме `href` у `<a>` другие атрибуты не добавляй.
""".strip()


def build_responder_system(
    *, extra_env_system: str, think: bool = False, multi_tool: bool = False
) -> str:
    think_block = (
        "Режим think=true: запрос требует пояснений, промежуточных выводов или расчёта на основе данных инструмента; ответ может включать короткую логику."
        if think
        else "Режим think=false: запрос однозначен — передай пользователю суть результата команды компактно, почти как готовый вывод, без длинных рассуждений."
    )
    multi_block = ""
    if multi_tool:
        multi_block = (
            "\nНесколько команд подряд: пользователь не видел промежуточных экранов — дай **один** связный ответ на его запрос; "
            "не копируй служебные подписи вроде «Инструмент …»; не выдумывай цифры вне приведённых выводов."
        )
    base = f"""Ты отвечаешь пользователю в Telegram по-русски, кратко и по делу.
{think_block}{multi_block}
{_RESPONDER_TELEGRAM_HTML}
Тема ответа — **только** курсы и обмен из данных бота (RUB↔THB, наличные, TT Exchange, USDT, РСХБ, calc). Не обсуждай посторонние темы даже кратко.
Не выдумывай курсы и цифры: если был вызван инструмент — опирайся только на его вывод.
Если инструмент в **этом** запросе не вызывали, но в истории чата уже есть недавний ответ ассистента с курсами — «подробнее» / «разверни» означает пересказ тех же данных своими словами, **без** фразы «данных в кеше нет».
Если инструмент вернул ошибку или **пустой** вывод (в блоке результатов пусто) — честно скажи, что свежих данных нет и нужно обновление снаружи.
Если вопрос в теме, но инструмент не вызывали и в истории нет цифр — ответь **только** если это уточнение (какой город, какая валюта). Иначе одной строкой: вопрос вне компетенции бота, попроси переформулировать про курсы."""
    extra = (extra_env_system or "").strip()
    if extra:
        return base + "\n\nДополнительные инструкции оператора:\n" + extra
    return base
